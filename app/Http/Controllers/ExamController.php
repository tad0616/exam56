    {
        $this->validate($request, [
            'title' => 'required|min:2|max:255',
        ], [
            'required' => '「:attribute」為必填欄位',
            'min'      => '「:attribute」至少要 :min 個字',
            'max'      => '「:attribute」最多只能 :max 個字',
        ]);
        Exam::create($request->all());
        return redirect()->route('exam.index');
    }

